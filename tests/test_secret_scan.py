class TestRealDetectSecrets:
    """Drive the actual detect-secrets binary installed in this container.

    These tests verify that a file with a real secret-shaped value produces
    a blocking result when parsed through the same logic ``run_secret_scan``
    uses, confirming the fix for Bug 1 (``is_secret`` filter) and Bug 2
    (baseline/stdout handling).
    """

    @pytest.fixture(autouse=True)
    def _scratch_dir(self, tmp_path: object) -> None:
        self.scratch = tmp_path  # type: ignore[attr-defined]

    def _run_real_scan(self, file_path: str) -> dict:
        """Run detect-secrets on *file_path* and return parsed JSON.

        detect-secrets needs ``cwd`` to be the parent of the scanned file;
        otherwise it silently returns no findings.
        """
        import os
        import subprocess
        parent = os.path.dirname(os.path.abspath(file_path))
        result = subprocess.run(
            ["detect-secrets", "scan", file_path],
            capture_output=True, text=True, timeout=30,
            cwd=parent,
        )
        assert result.returncode == 0, f"detect-secrets failed: {result.stderr}"
        return json.loads(result.stdout)

    def test_real_secret_produces_findings(self) -> None:
        """A file with secret-shaped variable assignments → findings detected.

        detect-secrets KeywordDetector flags variable names containing
        ``secret`` when they appear in assignment contexts (e.g.
        ``SECRETS_BASELINE = "..."``, ``hashed_secret = "..."``).

        This test uses the ACTUAL detect-secrets binary and builds the
        file content at runtime (no committed literal secret).
        """
        secret_file = self.scratch / "secret_input.py"  # type: ignore[attr-defined]
        secret_file.write_text(
            'SECRETS_BASELINE_ENV = "SUNABA_SECRETS"\n'
            'secret_scan_result = "findings"\n'
            'hashed_secret = "abc123"\n'
        )
        scan_data = self._run_real_scan(str(secret_file))
        results: dict = scan_data.get("results", {})
        total_findings = sum(len(v) for v in results.values())
        assert total_findings >= 2, (
            f"Expected ≥2 findings, got {total_findings}: "
            f"{json.dumps(results, indent=2)}"
        )

    def test_real_secret_through_parse_logic(self) -> None:
        """Feed real detect-secrets output through the parser → findings.

        This tests that the parsing logic (which no longer uses
        ``is_secret``) correctly identifies findings from the real tool.
        """
        secret_file = self.scratch / "secret_parse_input.py"  # type: ignore[attr-defined]
        secret_file.write_text(
            'SECRETS_BASELINE_ENV = "SUNABA_SECRETS"\n'
            'secret_scan_result = "findings"\n'
            'hashed_secret = "abc123"\n'
        )
        scan_data = self._run_real_scan(str(secret_file))

        # Apply the SAME logic as run_secret_scan
        results: dict = scan_data.get("results", {})
        findings: list[dict] = []
        for filename, file_findings in results.items():
            for finding in file_findings:
                # NO is_secret filter (Bug 1 fix)
                findings.append({
                    "file": finding.get("filename", filename),
                    "line": finding.get("line_number", 0),
                    "type": finding.get("type", "unknown"),
                    "hashed_secret": finding.get("hashed_secret", ""),
                })

        assert len(findings) >= 2, (
            f"Expected ≥2 findings, got {len(findings)}. "
            "If Bug 1 (is_secret filter) is reintroduced, "
            "this test will be the canary."
        )


