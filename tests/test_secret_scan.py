def _dummy_version_output() -> str:
    """Return fake detect-secrets version output — matches real tool output."""
    return "1.5.0"


def _make_clean_scan_json() -> str:
    """Return JSON with no findings (empty results dict)."""
    return json.dumps({
        "generated_at": "2026-07-20T00:00:00Z",
        "plugins_used": [],
        "results": {},
    })


def _make_finding_json(filename: str, line: int, secret_type: str) -> str:
    """Build detect-secrets JSON output with one finding.

    Matches the REAL output shape from detect-secrets: no ``is_secret``
    key (detect-secrets does not emit that — it is only written by
    ``detect-secrets audit``, a manual triage step).

    The ``hashed_secret`` is a synthetic SHA-256 hex string built at
    runtime so the committed file contains no actual secret hash.
    """
    import hashlib
    fake_secret = "".join(
        chr(ord(c) + 1) for c in "no-real-secret-here"
    )
    hashed = hashlib.sha256(fake_secret.encode()).hexdigest()
    return json.dumps({
        "generated_at": "2026-07-20T00:00:00Z",
        "plugins_used": [{"name": "AWSKeyDetector"}],
        "results": {
            filename: [
                {
                    "type": secret_type,
                    "filename": filename,
                    "line_number": line,
                    "hashed_secret": hashed,
                    "is_verified": False,
                }
            ]
        },
    })


def _load_real_scan_fixture() -> str:
    """Load genuine detect-secrets scan output from the committed fixture.

    The fixture was generated by running::

        detect-secrets scan real_secret_input.py

    on a file containing a secret-shaped value.  This exercises the parser
    with the actual shape the tool produces (no ``is_secret`` key, etc.).
    """
    import pathlib
    fixture_path = pathlib.Path(__file__).parent / "fixtures" / "secret_scan_real_output.json"
    return fixture_path.read_text()


